<template>
  <div class="word">
    <h3>国家秘密定密管理暂行规定</h3>
    <h4>第一章　总　　则</h4>
    <p>
      <span>第一条</span
      >　为加强国家秘密定密管理，规范定密行为，根据《中华人民共和国保守国家秘密法》（以下简称保密法）及其实施条例，制定本规定。
    </p>
    <p>
      <span>第二条</span
      >　本规定所称定密，是指国家机关和涉及国家秘密的单位（以下简称机关、单位）依法确定、变更和解除国家秘密的活动。
    </p>
    <p>
      <span>第三条</span
      >　机关、单位定密以及定密责任人的确定、定密授权和定密监督等工作，适用本规定。
    </p>
    <p>
      <span>第四条</span
      >　机关、单位定密应当坚持最小化、精准化原则，做到权责明确、依据充分、程序规范、及时准确，既确保国家秘密安全，又便利信息资源合理利用。
    </p>
    <p>
      <span>第五条</span
      >　机关、单位应当依法开展定密工作，建立健全相关管理制度，定期组织培训和检查，接受保密行政管理部门和上级机关、单位或者业务主管部门的指导和监督。
    </p>
    <h4>第二章　定密授权</h4>
    <p>
      <span>第六条</span
      >　中央国家机关、省级机关以及设区的市、自治州一级的机关（以下简称授权机关）可以根据工作需要或者机关、单位申请作出定密授权。
    </p>
    <p>保密行政管理部门应当将授权机关名单在有关范围内公布。</p>
    <p>
      <span>第七条</span
      >　中央国家机关可以在主管业务工作范围内作出授予绝密级、机密级和秘密级国家秘密定密权的决定。省级机关可以在主管业务工作范围内或者本行政区域内作出授予绝密级、机密级和秘密级国家秘密定密权的决定。设区的市、自治州一级的机关可以在主管业务工作范围内或者本行政区域内作出授予机密级和秘密级国家秘密定密权的决定。
    </p>
    <p>定密授权不得超出授权机关的定密权限。被授权机关、单位不得再行授权。</p>
    <p>
      <span>第八条</span
      >　授权机关根据工作需要，可以对承担本机关定密权限内的涉密科研、生产或者其他涉密任务的机关、单位，就具体事项作出定密授权。
    </p>
    <p>
      <span>第九条</span
      >　没有定密权但经常产生国家秘密事项的机关、单位，或者虽有定密权但经常产生超出其定密权限的国家秘密事项的机关、单位，可以向授权机关申请定密授权。
    </p>
    <p>
      机关、单位申请定密授权，应当向其上级业务主管部门提出；没有上级业务主管部门的，应当向其上级机关提出。
    </p>
    <p>
      机关、单位申请定密授权，应当书面说明拟申请的定密权限、事项范围、授权期限以及申请依据和理由。
    </p>
    <p>
      <span>第十条</span
      >　授权机关收到定密授权申请后，应当依照保密法律法规和国家秘密及其密级的具体范围（以下简称保密事项范围）进行审查。对符合授权条件的，应当作出定密授权决定；对不符合授权条件的，应当作出不予授权的决定。
    </p>
    <p>
      定密授权决定应当以书面形式作出，明确被授权机关、单位的名称和具体定密权限、事项范围、授权期限。
    </p>
    <p>
      <span>第十一条</span
      >　授权机关应当对被授权机关、单位行使所授定密权情况进行监督，对发现的问题及时纠正。
    </p>
    <p>
      保密行政管理部门发现定密授权不当或者被授权机关、单位对所授定密权行使不当的，应当通知有关机关、单位纠正。
    </p>
    <p>
      <span>第十二条</span
      >　被授权机关、单位不再经常产生授权范围内的国家秘密事项，或者因保密事项范围调整授权事项不再作为国家秘密的，授权机关应当及时撤销定密授权。
    </p>
    <p>
      因保密事项范围调整授权事项密级发生变化的，授权机关应当重新作出定密授权。
    </p>
    <p>
      <span>第十三条</span
      >　中央国家机关、省级机关作出的授权决定和撤销授权决定，报国家保密行政管理部门备案。设区的市、自治州一级的机关作出的授权决定和撤销授权决定，报省、自治区、直辖市保密行政管理部门备案。
    </p>
    <p>
      机关、单位收到定密授权决定或者撤销定密授权决定后，应当报同级保密行政管理部门备案。
    </p>
    <h4>第三章　定密责任人</h4>
    <p>
      <span>第十四条</span
      >　机关、单位负责人为本机关、本单位的定密责任人，对定密工作负总责。根据工作需要，机关、单位负责人可以指定本机关、本单位其他负责人、内设机构负责人或者其他工作人员为定密责任人，并明确相应的定密权限。
    </p>
    <p>
      机关、单位指定的定密责任人应当熟悉涉密业务工作，符合在涉密岗位工作的基本条件。
    </p>
    <p>
      <span>第十五条</span
      >　机关、单位应当在本机关、本单位内部公布定密责任人名单及其定密权限，并报同级保密行政管理部门备案。
    </p>
    <p>
      <span>第十六条</span
      >　机关、单位定密责任人和承办人应当接受定密培训，熟悉定密职责和保密事项范围，掌握定密程序和方法。
    </p>
    <p>
      <span>第十七条</span
      >　机关、单位负责人发现其指定的定密责任人未依法履行定密职责的，应当及时纠正；有下列情形之一的，应当作出调整：
    </p>
    <p>（一）定密不当，情节严重的；</p>
    <p>（二）因离岗离职无法继续履行定密职责的；</p>
    <p>（三）保密行政管理部门建议调整的；</p>
    <p>（四）因其他原因不宜从事定密工作的。</p>
    <h4>第四章　国家秘密确定</h4>
    <p>
      <span>第十八条</span
      >　机关、单位确定国家秘密应当依据保密事项范围进行。保密事项范围没有明确规定但属于保密法第九条、第十条规定情形的，应当确定为国家秘密。
    </p>
    <p><span>第十九条</span>　下列事项不得确定为国家秘密：</p>
    <p>（一）需要社会公众广泛知晓或者参与的；</p>
    <p>（二）属于工作秘密、商业秘密、个人隐私的；</p>
    <p>（三）已经依法公开或者无法控制知悉范围的；</p>
    <p>（四）法律、法规或者国家有关规定要求公开的。</p>
    <p>
      <span>第二十条</span
      >　机关、单位对所产生的国家秘密事项有定密权的，应当依法确定密级、保密期限和知悉范围。没有定密权的，应当先行采取保密措施，并立即报请有定密权的上级机关、单位确定；没有上级机关、单位的，应当立即提请有相应定密权限的业务主管部门或者保密行政管理部门确定。
    </p>
    <p>
      机关、单位执行上级机关、单位或者办理其他机关、单位已定密事项所产生的国家秘密事项，根据所执行或者办理的国家秘密事项确定密级、保密期限和知悉范围。
    </p>
    <p>
      <span>第二十一条</span
      >　机关、单位确定国家秘密，应当依照法定程序进行并作出书面记录，注明承办人、定密责任人和定密依据。
    </p>
    <p>
      <span>第二十二条</span
      >　国家秘密具体的保密期限一般应当以日、月或者年计；不能确定具体的保密期限的，应当确定解密时间或者解密条件。国家秘密的解密条件应当明确、具体、合法。
    </p>
    <p>除保密事项范围有明确规定外，国家秘密的保密期限不得确定为长期。</p>
    <p>
      <span>第二十三条</span
      >　国家秘密的知悉范围应当在国家秘密载体上标明。不能标明的，应当书面通知知悉范围内的机关、单位或者人员。
    </p>
    <p>
      <span>第二十四条</span
      >　国家秘密一经确定，应当同时在国家秘密载体上作出国家秘密标志。国家秘密标志形式为“密级★保密期限”、“密级★解密时间”或者“密级★解密条件”。
    </p>
    <p>
      在纸介质和电子文件国家秘密载体上作出国家秘密标志的，应当符合有关国家标准。没有国家标准的，应当标注在封面左上角或者标题下方的显著位置。光介质、电磁介质等国家秘密载体和属于国家秘密的设备、产品的国家秘密标志，应当标注在壳体及封面、外包装的显著位置。
    </p>
    <p>国家秘密标志应当与载体不可分离，明显并易于识别。</p>
    <p>
      无法作出或者不宜作出国家秘密标志的，确定该国家秘密的机关、单位应当书面通知知悉范围内的机关、单位或者人员。凡未标明保密期限或者解密条件，且未作书面通知的国家秘密事项，其保密期限按照绝密级事项30年、机密级事项20年、秘密级事项10年执行。
    </p>
    <p>
      <span>第二十五条</span
      >　两个以上机关、单位共同产生的国家秘密事项，由主办该事项的机关、单位征求协办机关、单位意见后确定。
    </p>
    <p>临时性工作机构的定密工作，由承担该机构日常工作的机关、单位负责。</p>
    <h4>第五章　国家秘密变更</h4>
    <p>
      <span>第二十六条</span
      >　有下列情形之一的，机关、单位应当对所确定国家秘密事项的密级、保密期限或者知悉范围及时作出变更：
    </p>
    <p>（一）定密时所依据的法律法规或者保密事项范围发生变化的；</p>
    <p>（二）泄露后对国家安全和利益的损害程度发生明显变化的。</p>
    <p>
      必要时，上级机关、单位或者业务主管部门可以直接变更下级机关、单位确定的国家秘密事项的密级、保密期限或者知悉范围。
    </p>
    <p>
      <span>第二十七条</span
      >　机关、单位认为需要延长所确定国家秘密事项保密期限的，应当在保密期限届满前作出决定；延长保密期限使累计保密期限超过保密事项范围规定的，应当报规定该保密事项范围的中央有关机关批准，中央有关机关应当在接到报告后30日内作出决定。
    </p>
    <p>
      <span>第二十八条</span
      >　国家秘密知悉范围内的机关、单位，其有关工作人员不在知悉范围内，但因工作需要知悉国家秘密的，应当经机关、单位负责人批准。
    </p>
    <p>
      国家秘密知悉范围以外的机关、单位及其人员，因工作需要知悉国家秘密的，应当经原定密机关、单位同意。
    </p>
    <p>原定密机关、单位对扩大知悉范围有明确规定的，应当遵守其规定。</p>
    <p>扩大国家秘密知悉范围应当作出详细记录。</p>
    <p>
      <span>第二十九条</span
      >　国家秘密变更按照国家秘密确定程序进行并作出书面记录。
    </p>
    <p>
      国家秘密变更后，原定密机关、单位应当及时在原国家秘密标志附近重新作出国家秘密标志。
    </p>
    <p>
      <span>第三十条</span
      >　机关、单位变更国家秘密的密级、保密期限或者知悉范围的，应当书面通知知悉范围内的机关、单位或者人员。有关机关、单位或者人员接到通知后，应当在国家秘密标志附近标明变更后的密级、保密期限和知悉范围。
    </p>
    <p>
      延长保密期限的书面通知，应当于原定保密期限届满前送达知悉范围内的机关、单位或者人员。
    </p>
    <h4>第六章　国家秘密解除</h4>
    <p>
      <span>第三十一条</span
      >　机关、单位应当每年对所确定的国家秘密进行审核，有下列情形之一的，及时解密：
    </p>
    <p>（一）保密法律法规或者保密事项范围调整后，不再属于国家秘密的；</p>
    <p>（二）公开后不会损害国家安全和利益，不需要继续保密的。</p>
    <p>
      机关、单位经解密审核，对本机关、本单位或者下级机关、单位尚在保密期限内的国家秘密事项决定公开的，正式公布即视为解密。
    </p>
    <p>
      <span>第三十二条</span
      >　国家秘密的具体保密期限已满、解密时间已到或者符合解密条件的，自行解密。
    </p>
    <p>
      <span>第三十三条</span
      >　保密事项范围明确规定保密期限为长期的国家秘密事项，机关、单位不得擅自解密；确需解密的，应当报规定该保密事项范围的中央有关机关批准，中央有关机关应当在接到报告后30日内作出决定。
    </p>
    <p>
      <span>第三十四条</span
      >　除自行解密的外，国家秘密解除应当按照国家秘密确定程序进行并作出书面记录。
    </p>
    <p>
      国家秘密解除后，有关机关、单位或者人员应当及时在原国家秘密标志附近作出解密标志。
    </p>
    <p>
      <span>第三十五条</span
      >　除自行解密和正式公布的外，机关、单位解除国家秘密，应当书面通知知悉范围内的机关、单位或者人员。
    </p>
    <p>
      <span>第三十六条</span
      >　机关、单位对所产生的国家秘密事项，解密之后需要公开的，应当依照信息公开程序进行保密审查。
    </p>
    <p>
      机关、单位对已解密的不属于本机关、本单位产生的国家秘密事项，需要公开的，应当经原定密机关、单位同意。
    </p>
    <p>
      机关、单位公开已解密的文件资料，不得保留国家秘密标志。对国家秘密标志以及属于敏感信息的内容，应当作删除、遮盖等处理。
    </p>
    <p>
      <span>第三十七条</span
      >　机关、单位对拟移交各级国家档案馆的尚在保密期限内的国家秘密档案，应当进行解密审核，对本机关、本单位产生的符合解密条件的档案，应当予以解密。
    </p>
    <p>
      已依法移交各级国家档案馆的属于国家秘密的档案，其解密办法由国家保密行政管理部门会同国家档案行政管理部门另行制定。
    </p>
    <h4>第七章　定密监督</h4>
    <p>
      <span>第三十八条</span
      >　机关、单位应当定期对本机关、本单位定密以及定密责任人履行职责、定密授权等定密制度落实情况进行检查，对发现的问题及时纠正。
    </p>
    <p>
      <span>第三十九条</span
      >　机关、单位应当向同级保密行政管理部门报告本机关、本单位年度国家秘密事项统计情况。
    </p>
    <p>
      下一级保密行政管理部门应当向上一级保密行政管理部门报告本行政区域年度定密工作情况。
    </p>
    <p>
      <span>第四十条</span
      >　中央国家机关应当依法对本系统、本行业的定密工作进行指导和监督。
    </p>
    <p>
      上级机关、单位或者业务主管部门发现下级机关、单位定密不当的，应当及时通知其纠正，也可以直接作出确定、变更或者解除的决定。
    </p>
    <p>
      <span>第二十四条</span
      >　国家秘密一经确定，应当同时在国家秘密载体上作出国家秘密标志。国家秘密标志形式为“密级★保密期限”、“密级★解密时间”或者“密级★解密条件”。
    </p>
    <p>
      <span>第四十一条</span
      >　保密行政管理部门应当依法对机关、单位定密工作进行指导、监督和检查，对发现的问题及时纠正或者责令整改。
    </p>
    <h4>第八章　法律责任</h4>
    <p>
      <span>第四十二条</span
      >　定密责任人和承办人违反本规定，有下列行为之一的，机关、单位应当及时纠正并进行批评教育；造成严重后果的，依纪依法给予处分：
    </p>
    <p>（一）应当确定国家秘密而未确定的；</p>
    <p>（二）不应当确定国家秘密而确定的；</p>
    <p>（三）超出定密权限定密的；</p>
    <p>（四）未按照法定程序定密的；</p>
    <p>（五）未按规定标注国家秘密标志的；</p>
    <p>（六）未按规定变更国家秘密的密级、保密期限、知悉范围的；</p>
    <p>（七）未按要求开展解密审核的；</p>
    <p>（八）不应当解除国家秘密而解除的；</p>
    <p>（九）应当解除国家秘密而未解除的；</p>
    <p>（十）违反本规定的其他行为。</p>
    <p>
      <span>第四十三条</span
      >　机关、单位未依法履行定密管理职责，导致定密工作不能正常进行的，应当给予通报批评；造成严重后果的，应当依法追究直接负责的主管人员和其他直接责任人员的责任。
    </p>
    <h4>第九章　附　　则</h4>
    <p><span>第四十四条</span>　本规定下列用语的含义：</p>
    <p>
      （一）“中央国家机关”包括中国共产党中央机关及部门、各民主党派中央机关、全国人大机关、全国政协机关、最高人民法院、最高人民检察院，国务院及其组成部门、直属特设机构、直属机构、办事机构、直属事业单位、部委管理国家局，以及中央机构编制管理部门直接管理机构编制的群众团体机关；
    </p>
    <p>
      （二）“省级机关”包括省（自治区、直辖市）党委、人大、政府、政协机关，以及人民法院、人民检察院；
    </p>
    <p>
      （三）“设区的市和自治州一级的机关”包括地（市、州、盟、区）党委、人大、政府、政协机关，以及人民法院、人民检察院，省（自治区、直辖市）直属机关和人民团体，中央国家机关设在省（自治区、直辖市）的直属机构，省（自治区、直辖市）在地区、盟设立的派出机构；
    </p>
    <p>
      （四）第九条所指“经常”，是指近3年来年均产生6件以上国家秘密事项的情形。
    </p>
    <p>
      <span>第四十五条</span
      >　各地区各部门可以依据本规定，制定本地区本部门国家秘密定密管理的具体办法。
    </p>
    <p>
      <span>第四十六条</span
      >　公安、国家安全机关定密授权和定密责任人确定的具体办法，由国家保密行政管理部门会同国务院公安、国家安全部门另行制定。
    </p>
    <p>
      <span>第四十七条</span
      >　本规定自公布之日起施行。1990年9月19日国家保密局令第2号发布的《国家秘密保密期限的规定》和1990年10月6日国家保密局、国家技术监督局令第3号发布的《国家秘密文件、资料和其他物品标志的规定》同时废止。
    </p>
